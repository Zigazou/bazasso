CREATE TABLE sirene
    -- Base Sirene épurée
    ( siren             VARCHAR(9)   -- Identifiant de l'entreprise
    , nic               VARCHAR(5)   -- Numéro interne de classement de
                                     -- l'entreprise
    , epci              VARCHAR(9)   -- Localisation de l'établissement dans un
                                     -- établissement public de coopération
                                     -- intercommunale
