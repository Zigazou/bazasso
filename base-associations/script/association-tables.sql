    , prodet            CHAR(1)      -- Caractère productif de l'établissement
                                     -- O=Productif, N=Non productif
    , prodpart          CHAR(1)      -- Participation particulière à la
                                     -- production de l'établissement.
                                     -- 1=Locataire d'un fond de commerce,
                                     -- 2=Loueur d'un fond de commerce,
                                     -- 3=Prestataire de personnel
