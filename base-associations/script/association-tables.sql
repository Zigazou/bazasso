                                     -- D=Donneur d'ordre, R=Réparation, 
                                     -- M=Montage, installation, S=activité
                                     -- simple
    , proden            CHAR(1)      -- Caractère productif de l'entreprise
                                     -- O=Productif, N=Non productif
